const curatedQuotes = quoteSeeds.map((quote, index) => ({
  id: String(index + 1),
  ...quote,
})) satisfies ReadingQuote[];

function readQuoteHistory(now: number) {
  try {
    const rawHistory = localStorage.getItem(QUOTE_HISTORY_KEY);
    const history = rawHistory ? (JSON.parse(rawHistory) as QuoteHistoryItem[]) : [];
    return history.filter((item) => now - item.selectedAt < ONE_WEEK_MS);
  } catch {
    return [];
  }
}

function writeQuoteHistory(history: QuoteHistoryItem[]) {
  try {
    localStorage.setItem(QUOTE_HISTORY_KEY, JSON.stringify(history));
  } catch {
    // localStorage may be unavailable in rare webview privacy modes.
  }
}

export function selectReadingQuote() {
  const now = Date.now();
  const recentHistory = readQuoteHistory(now);
  const recentIds = new Set(recentHistory.map((item) => item.id));
  const candidates = curatedQuotes.filter((quote) => !recentIds.has(quote.id));
  const availableQuotes = candidates.length > 0 ? candidates : curatedQuotes;
  const selected =
    availableQuotes[Math.floor(Math.random() * availableQuotes.length)] ?? curatedQuotes[0]!;

  writeQuoteHistory([{ id: selected.id, selectedAt: now }, ...recentHistory].slice(0, 128));
  return selected;
}

